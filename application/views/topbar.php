<script type="text/javascript">
$(function() {
        $('#search_input').on('keyup paste', function() {
        var q = $(this).val();
        var search_results = $('#search_results');
         if (q == '') {
            search_results.html('');
            return;
        }
        if ( q.length < 3 ) {
            search_results.html('');
            return;
        }
        setTimeout(
        $.ajax({
            url: "<?php echo base_url();?>index.php?admin/search",
            type: "POST",
            data: {'<?php echo $this->security->get_csrf_token_name(); ?>':'<?php echo $this->security->get_csrf_hash(); ?>',q},
            success: function(results){
                $('#loading').hide();
                search_results.html(results);
            },
            error:function(){
                search_results.html('There is a problem');
            }
         })
         ,4000);
       });
       
       $("body").click(function(e) {
            if (!$(e.target).parents('#top_search_dropdown').hasClass('search-results')) {
                $('#top_search_dropdown').remove();
                $('#search_input').val('');
            }
        });
       

});
</script>
